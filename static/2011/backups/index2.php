<h3>ABPro2011発表者募集：</h3><br />
　「普通じゃないプログラム作品」の発表を募集します．<br />
　高度な技術や専門的な知識を用いるのも良いですが，我々が目指すのは，人を驚かせ，笑わせ，幸せにするようなプログラムです．<br />
<br />　
<h3>発表申し込み時の注意：</h3><br />
　参加単位は「個人」となっております．<br />
　発表は，原則作成者が行ってください．<br />
　発表するものは，「プログラムなら何でも可」とします．ハードウェアであるかソフトウェアであるかは問いません．<br />
<br />　
<h3>発表時の注意：</h3><br />
　発表時間は厳守をお願いいたします．<br />
　ネットワーク接続については約束できません．最悪ネットワークに繋がらなくてもローカルで動くものを発表してください．<br />
　発表に関して，当日会場で発表できない場合は，ビデオレターでの発表を許可致します．ただし，「会場に作品を持ち込みできない」などの理由で作品をビデオ映像だけで発表することは認めません．<br />
　Ustreamなどの映像配信は行いません．Twitterハッシュタグを用いた実況用のスクリーンは用意する予定です．<br />
